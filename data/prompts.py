import random
import re

TASK_PROMPT_PREFIX_PATTERN = re.compile(r"<task:\s*[^>]+>:\s*")
DETECT_QUERY_PATTERN = re.compile(r"<p>(.*?)</p>", flags=re.IGNORECASE | re.DOTALL)
SEG_FORMAT_PHRASE = "in the EXACT format: <p>instance-no<color>(R,G,B)</color></p>"


def ensure_image_placeholders(question: str, image_count: int) -> str:
    question = str(question or "").strip()
    token_count = question.count("<image>")
    if token_count == image_count:
        return question
    if token_count < image_count:
        prefix = " ".join(["<image>"] * (image_count - token_count))
        return f"{prefix} {question}".strip()
    raise ValueError(
        f"The number of `<image>` tokens must match the number of images. "
        f"Got {token_count} image tokens but {image_count} images."
    )


def _remove_task_prompt_prefix(text: str) -> str:
    return TASK_PROMPT_PREFIX_PATTERN.sub("", text)


def _extract_detect_categories(text: str) -> str:
    phrases = []
    for match in DETECT_QUERY_PATTERN.finditer(text):
        phrase_with_tag = re.sub(r"\s+", " ", match.group(0)).strip()
        if phrase_with_tag:
            phrases.append(phrase_with_tag)
    return ", ".join(phrases)


def _replace_task_prompt_in_place(text: str, replacement: str) -> str:
    match = TASK_PROMPT_PREFIX_PATTERN.search(text)
    if match is None:
        return text
    return text[: match.start()] + replacement


def strip_task_prompt(text: str, task_prompt="raw_prompt") -> str:
    """
    Supports three task_prompt modes:
    1) raw_prompt / True: keep the original JSONL prompt.
    2) no_prefix_promt / False: remove only the `<task: xxx>: ` prefix.
    3) keys in NATURE_PRMOPT_TEMPLATE: randomly replace the task prefix with a
       natural-language template, filling detection `{categories}` from `<p>...</p>` tags.
    """
    if isinstance(task_prompt, bool):
        task_prompt = "raw_prompt" if task_prompt else "no_prefix_promt"
    elif task_prompt is None:
        task_prompt = "raw_prompt"

    if task_prompt == "raw_prompt":
        return text
    if task_prompt == "no_prefix_promt":
        return _remove_task_prompt_prefix(text)

    if task_prompt in NATURE_PRMOPT_TEMPLATE:
        if random.random() >= 0.5:
            return _remove_task_prompt_prefix(text)

        template = random.choice(NATURE_PRMOPT_TEMPLATE[task_prompt])
        if "{categories}" in template:
            categories = _extract_detect_categories(text)
            if not categories:
                categories = "all relevant objects"
            template = template.replace("{categories}", categories)

        return _replace_task_prompt_in_place(text, template)

    return text


def format_seg_task_prompt(text: str, task_prompt="nature_promt") -> str:
    if task_prompt == "nature_promt":
        if random.random() < 0.5 and SEG_FORMAT_PHRASE in text:
            return text.replace(SEG_FORMAT_PHRASE, "", 1).strip()
        return text

    if task_prompt == "no_prefix_promt":
        return text

    raise ValueError(f"Unsupported segmentation task_prompt mode: {task_prompt}")


